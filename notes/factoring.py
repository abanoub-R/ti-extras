# to factor a trinomial
#   1. find the gcf
#   2. divide it out of the trinomial to where a=1
#   3. use the ac method
#       - as in find 2 numbers that add up to b and multiply to c
#   4. the results from above are your factors in x + a form
#
# you can also plug into the quadratic formula, 
# but that may not work if the discriminant is negative
